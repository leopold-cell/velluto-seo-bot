_STATE_PATH = os.path.join(os.path.dirname(os.path.abspath(__file__)), "nl_keywords_used.json")


def get_next_nl_keyword() -> dict | None:
    """Return the next unused keyword, Phase 1 → 2 → 3 order."""
    state = json.load(open(_STATE_PATH)) if os.path.exists(_STATE_PATH) else {"used": []}
    used = set(state.get("used", []))
    for item in NL_QUEUE:
        if item["keyword"] not in used:
            return item
    return None  # All keywords covered — cycle resets


def mark_nl_keyword_used(keyword: str):
    state = json.load(open(_STATE_PATH)) if os.path.exists(_STATE_PATH) else {"used": []}
    used = state.get("used", [])
    if keyword not in used:
        used.append(keyword)
    state["used"] = used
    json.dump(state, open(_STATE_PATH, "w"), indent=2)


def get_nl_queue_status() -> dict:
    state = json.load(open(_STATE_PATH)) if os.path.exists(_STATE_PATH) else {"used": []}
    used = set(state.get("used", []))
    by_phase = {1: {"total": 0, "done": 0}, 2: {"total": 0, "done": 0}, 3: {"total": 0, "done": 0}}
    for item in NL_QUEUE:
        p = item["phase"]
        by_phase[p]["total"] += 1
        if item["keyword"] in used:
            by_phase[p]["done"] += 1
    return {"used": len(used), "total": len(NL_QUEUE), "by_phase": by_phase}


